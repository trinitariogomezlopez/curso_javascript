function mostrarError(campo, divError, divOk, mensaje) {
    campo.classList.remove('correcto');
    campo.classList.add('error');

    divError.textContent = 'вљ  ' + mensaje;
    divError.style.display = 'block';

    divOk.style.display = 'none';
}

